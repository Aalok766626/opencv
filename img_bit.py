import cv2
import numpy as aalok
square=aalok.ones((600,600),aalok.uint8)
rect=cv2.rectangle(square,(100,100),(500,500),255,10)
ellipse=aalok.ones((600,600),aalok.uint8)
cv2.ellipse(ellipse,(300,300),(300,250),60,0,360,255,10)
cv2.imshow('Square',square)
cv2.waitKey(0)
cv2.imshow('Ellipse',ellipse)
cv2.waitKey(0)
oor=cv2.bitwise_or(square,ellipse)
cv2.imshow('Or',oor)
cv2.waitKey(0)
anda=cv2.bitwise_and(square,ellipse)
cv2.imshow('And',anda)
cv2.waitKey(0)
xor=cv2.bitwise_xor(square,ellipse)
cv2.imshow('Xor',xor)
cv2.waitKey(0)
cv2.destroyAllWindows()